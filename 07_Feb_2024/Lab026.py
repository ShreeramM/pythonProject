# Operators
# Assignment Operator
name = "Pramod"  # Assignment operator will store the value of the literal to the identifier

# Unary Operator - Single value or literal
age = 95
my_Bank_Balance = -100
print(age)
print(my_Bank_Balance)

# Not Operator - Unary
is_Married = True
print(is_Married)
print(not is_Married)  # Reverse of value, not will work with Booleans i.e Negatiations

#is operator - Identity
a = 5
b = 5
c = False
print(a is b) #True
print(a is c) #False

my_list = [1,2,3]
my_list2 = [1,2,3]
print(my_list is my_list2) #list are stored in different location in memory




