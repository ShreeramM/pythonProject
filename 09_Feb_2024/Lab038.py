# Loops
# Repeated Action to perform

print("Hello World")
print("Hello World")
print("Hello World")
print("Hello World")
print("-------------")
for i in range(1, 10):  # 1 to 10 -1 i.e till 9 times
    print("Hello World")

for i in range(5):  # 0 to 4 -1 i.e till 5 times
    print(i)
print("-------------")
for i in range(3, 5):  # 0 to 4 -1 i.e till 5 times
    print(i)
print("-------------")
for i in range(1, 10, 2):  # Odd Numbers
    print(i)
